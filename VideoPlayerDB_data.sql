CREATE TABLE Creator (
    ID INT,
    Intro varchar(100),
    post_num INT,
    follwer_num INT,
    PRIMARY KEY (ID),
    FOREIGN KEY (ID) REFERENCES Users (ID),
    FOREIGN KEY (ID) REFERENCES Videos (Creator_id)
);

CREATE TABLE Review (
    review_id INT,
    user_id	INT,
    video_id INT,
    content varchar(100),
    agree_number INT,
    disagree_number INT,
    post_time datetime,
    PRIMARY KEY (review_id, video_id),
    FOREIGN KEY (user_id) REFERENCES Users (ID),
    FOREIGN KEY (video_id) REFERENCES Videos (Video_id)
);


CREATE TABLE Following (
    creator_id INT,
    user_id INT,
    Follow_time datetime,
    FOREIGN KEY (creator_id) REFERENCES Creator (ID),
    FOREIGN KEY (user_id) REFERENCES Users (ID)
);


CREATE TABLE Preference (
    user_id INT,
    label_id INT,
    FOREIGN KEY (user_id) REFERENCES Users (ID),
    FOREIGN KEY (label_id) REFERENCES Labels (Label_id)
);

CREATE TABLE Video_label (
	label_id INT,
    video_id INT,
    FOREIGN KEY (video_id) REFERENCES Videos (Video_id),
    FOREIGN KEY (label_id) REFERENCES Labels (Label_id)
);

CREATE TABLE Playlist (
    user_id INT,
    video_id INT,
    add_time datetime,
    order_id INT,
    FOREIGN KEY (user_id) REFERENCES Users (ID),
    FOREIGN KEY (video_id) REFERENCES Videos (Video_id)
);

CREATE TABLE Favor_list (
    user_id INT,
    video_id INT,
    add_time datetime,
    FOREIGN KEY (user_id) REFERENCES Users (ID),
    FOREIGN KEY (video_id) REFERENCES Videos (Video_id)
);

CREATE TABLE History (
    user_id INT,
    video_id INT,
    last_stop_time datetime,
    last_play_time datetime,
    PRIMARY KEY (user_id, video_id),
    FOREIGN KEY (user_id) REFERENCES Users (ID),
    FOREIGN KEY (video_id) REFERENCES Videos (Video_id)
);

CREATE TABLE Comments (
    comment_id INT PRIMARY KEY AUTO_INCREMENT,
    video_id INT,
    review_id INT,
    user_id INT,
    content VARCHAR(255),
    agree_num INT,
    disagree_num INT,
    post_time DATETIME,
    FOREIGN KEY (video_id) REFERENCES Videos (video_id),
    FOREIGN KEY (user_id) REFERENCES Users (id)
);

